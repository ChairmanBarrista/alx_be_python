#This task is to write a Python script that converts a specific number of hours into seconds.
#This task reinforces the concept of arithmetic operations within a practical context.

hours = 2
seconds = (60 * 60) * 2 #there are 3600 seconds in an hour (since there are 60 minutes in an hour and 60 seconds in a minute, thus 60 x 60 = 3600).

print(hours, "hour(s) is", seconds, "seconds.")
